#!/usr/bin/env node
"use strict";
/**
 * Generator for the FirstCall Mechanical "Insights" blog.
 * Reads scripts/insights-data.js (the single source of truth) and regenerates
 * the 4 hookup places:
 *   (1) hub cards .............. insights.html       (grid, newest-first)
 *   (2) sitemap entries ........ sitemap.xml         (insights block)
 *   (3) "Related insights" links . inside every article (in a marked region)
 *   (4) branch "service" CTA ..... inside every article (in the same region)
 *
 * Idempotent. Run:  node scripts/build-insights.js
 * Enforces the Columbus-no-plumbing rule via SERVICES (and asserts it at the end).
 */
const fs = require("fs");
const path = require("path");
const data = require("./insights-data.js");

const ROOT = path.resolve(__dirname, "..");
const INS = path.join(ROOT, "insights");
const MONTHS = ["January","February","March","April","May","June","July","August","September","October","November","December"];
const longDate = iso => { const [y,m,d] = iso.split("-").map(Number); return `${MONTHS[m-1]} ${d}, ${y}`; };
const shortDate = iso => { const [y,m,d] = iso.split("-").map(Number); return `${MONTHS[m-1].slice(0,3)} ${d}, ${y}`; };

const posts = [...data.POSTS].sort((a,b) => b.date.localeCompare(a.date)); // newest first
const BRANCH_ORDER = ["columbus","dfw","central-texas"];

// ---- parity check: every article <-> manifest entry ----
const slugSet = new Set(data.POSTS.map(p => p.slug));
for (const f of fs.readdirSync(INS).filter(f => f.endsWith(".html"))) {
  if (!slugSet.has(f.replace(/\.html$/,""))) console.warn("  WARN: article has no manifest entry:", f);
}
for (const p of data.POSTS) {
  if (!fs.existsSync(path.join(INS, p.slug + ".html"))) console.warn("  WARN: manifest entry has no article:", p.slug);
}

// ---- related posts (3, same category preferred) ----
function relatedFor(p) {
  const same = posts.filter(q => q.slug !== p.slug && q.category === p.category);
  const other = posts.filter(q => q.slug !== p.slug && q.category !== p.category);
  return same.concat(other).slice(0, 3);
}

// ---- branch service CTA (enforces Columbus-no-plumbing via null targets) ----
function ctaFor(p) {
  const svc = data.SERVICES[p.service];
  if (!svc) return "";
  const branches = BRANCH_ORDER.filter(b => svc[b]);
  if (!branches.length) return "";
  if (branches.length === 1) {
    const b = branches[0];
    return `<p><strong>FirstCall self-performs ${svc.label} in <a href="${svc[b]}">${data.BRANCH_NAMES[b]}</a>.</strong></p>`;
  }
  const links = branches.map(b => `<a href="${svc[b]}">${data.BRANCH_NAMES[b]}</a>`).join(" &middot; ");
  return `<p><strong>Get ${svc.label} from FirstCall:</strong> ${links}</p>`;
}

// ---- the generated footer region inside each article ----
const F_START = "<!-- insights:footer (generated by scripts/build-insights.js — edit scripts/insights-data.js, then re-run) -->";
const F_END = "<!-- /insights:footer -->";
function regionFor(p) {
  const lines = ["            " + F_START];
  const cta = ctaFor(p);
  if (cta) lines.push("            " + cta);
  lines.push("            <h2>Related insights</h2>");
  lines.push("            <ul>");
  relatedFor(p).forEach(r => lines.push(`              <li><a href="/insights/${r.slug}">${r.title}</a></li>`));
  lines.push("            </ul>");
  lines.push("            " + F_END);
  return lines.join("\n");
}

// ---- (3)+(4) rewrite each article's footer region ----
let articleCount = 0;
for (const p of data.POSTS) {
  const fp = path.join(INS, p.slug + ".html");
  if (!fs.existsSync(fp)) continue;
  let h = fs.readFileSync(fp, "utf8");
  const region = regionFor(p);

  if (h.includes(F_START)) {
    // idempotent replace
    h = h.replace(/[ \t]*<!-- insights:footer[\s\S]*?<!-- \/insights:footer -->/, region);
  } else {
    // first run: strip the old service CTA <p> and old Related block, then insert
    h = h.replace(/\n[ \t]*<p><strong>(Get commercial|Get building|FirstCall self-performs commercial plumbing)[\s\S]*?<\/p>/, "");
    h = h.replace(/\n[ \t]*<h2>Related insights<\/h2>[\s\S]*?<\/ul>/, "");
    const byIdx = h.indexOf('<div class="article__byline">');
    let closeIdx = h.lastIndexOf("</div>", byIdx); // prose closing </div>
    let i = closeIdx;
    while (i > 0 && (h[i-1] === " " || h[i-1] === "\t")) i--;
    h = h.slice(0, i) + region + "\n" + h.slice(i);
  }
  fs.writeFileSync(fp, h, "utf8");
  articleCount++;
}

// ---- (1) hub cards ----
function hubCard(p) {
  const c = p.category;
  return [
    `          <article class="insight-card" data-cat="${c}">`,
    `            <a class="insight-card__media insight-card__media--${c}" href="/insights/${p.slug}" tabindex="-1" aria-hidden="true"></a>`,
    `            <div class="insight-card__body">`,
    `              <span class="tag tag--${c}">${data.CATEGORIES[c].label}</span>`,
    `              <h2 class="insight-card__title"><a href="/insights/${p.slug}">${p.title}</a></h2>`,
    `              <p class="insight-card__excerpt">${p.excerpt}</p>`,
    `              <div class="insight-card__meta"><time datetime="${p.date}">${shortDate(p.date)}</time> &middot; ${p.readTime}</div>`,
    `            </div>`,
    `          </article>`,
  ].join("\n");
}
const hubPath = path.join(ROOT, "insights.html");
let hub = fs.readFileSync(hubPath, "utf8");
const cards = posts.map(hubCard).join("\n\n");
hub = hub.replace(/(<div class="insights-grid" data-insights-grid>)[\s\S]*?(<div class="insights-empty")/, (m,g1,g2) => g1 + "\n\n" + cards + "\n\n        " + g2);
fs.writeFileSync(hubPath, hub, "utf8");

// ---- (2) sitemap insights block ----
const smUrl = (loc, lastmod, freq, prio) =>
  `  <url>\n    <loc>${loc}</loc>\n    <lastmod>${lastmod}</lastmod>\n    <changefreq>${freq}</changefreq>\n    <priority>${prio}</priority>\n  </url>`;
const newest = posts[0].date;
const smEntries = [smUrl(data.SITE + "/insights", newest, "weekly", "0.8")]
  .concat(posts.map(p => smUrl(`${data.SITE}/insights/${p.slug}`, p.date, "monthly", "0.7")))
  .join("\n");
const smPath = path.join(ROOT, "sitemap-mechanical.xml");
let sm = fs.readFileSync(smPath, "utf8");
const smBlock = "  <!-- FirstCall Mechanical — Insights (blog) -->\n" + smEntries + "\n</urlset>";
sm = sm.replace(/  <!-- FirstCall Mechanical[^\n]*Insights[^\n]*-->\n[\s\S]*?\n<\/urlset>/, () => smBlock);
fs.writeFileSync(smPath, sm, "utf8");

// ---- self-check: Columbus must never appear in any plumbing footer region ----
let violations = 0;
for (const p of data.POSTS.filter(p => p.service === "plumbing" || p.category === "plumbing")) {
  const h = fs.readFileSync(path.join(INS, p.slug + ".html"), "utf8");
  const m = h.match(/<!-- insights:footer[\s\S]*?<!-- \/insights:footer -->/);
  if (m && /columbus/i.test(m[0])) { violations++; console.error("  VIOLATION: Columbus referenced in plumbing footer:", p.slug); }
}
if (/insight-card__media--plumbing[\s\S]*?columbus/i.test("")) {} // (cards never link branches)

console.log(`Insights build complete:`);
console.log(`  articles footer-updated: ${articleCount}`);
console.log(`  hub cards: ${posts.length}`);
console.log(`  sitemap insights URLs: ${posts.length + 1}`);
console.log(`  Columbus-in-plumbing violations: ${violations}`);
if (violations) process.exit(1);
